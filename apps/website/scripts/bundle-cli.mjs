/**
 * Bundle the KSC check CLI into a single JS file for WebContainer.
 *
 * Produces apps/website/src/lib/lessons/cli-bundle.ts which exports
 * the bundle as a string constant. template.ts imports it and mounts
 * it in the WebContainer filesystem.
 *
 * typescript is marked external — it's installed via npm in the WebContainer.
 */

import * as esbuild from 'esbuild';
import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '../../..');

const result = await esbuild.build({
  entryPoints: [path.join(repoRoot, 'apps/website/scripts/wc-check-entry.ts')],
  bundle: true,
  platform: 'node',
  target: 'node18',
  format: 'esm',
  external: ['typescript'],
  write: false,
  minify: true,
  treeShaking: true,
  // Resolve workspace packages via node_modules symlinks
  absWorkingDir: repoRoot,
});

const bundleCode = result.outputFiles[0].text;

const outPath = path.join(repoRoot, 'apps/website/src/lib/lessons/cli-bundle.ts');
const tsContent = `// AUTO-GENERATED by bundle-cli.mjs — do not edit manually.
// Re-generate with: node apps/website/scripts/bundle-cli.mjs
export const CLI_BUNDLE = ${JSON.stringify(bundleCode)};
`;

fs.writeFileSync(outPath, tsContent);
console.log(`CLI bundle generated: ${(bundleCode.length / 1024).toFixed(1)} KB → ${outPath}`);
