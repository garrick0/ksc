// ── Helpers ──────────────────────────────────────────────────────────

function ksTypeName(kind: string): string {
  return `KS${kind}`;
}

/** Resolve a field's type ref to a KS TypeScript type string */
function resolveFieldType(field: FieldDesc, sumTypes: Map<string, SumTypeEntry>): string {
  if (field.tag === 'prop') return field.propType;

  const ref = field.typeRef;
  if (!ref) return 'KSNode'; // untyped = any node

  // If it's a sum type name, use the sum type alias
  if (sumTypes.has(ref)) return `KS${ref}`;

  // Otherwise it's a specific node kind
  return ksTypeName(ref);
}


