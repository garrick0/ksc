// ── SourceFile → CompilationUnit ──

function convertSourceFile(sf: ts.SourceFile): KS.KSCompilationUnit {
  const children: KS.KSNode[] = [];
  ts.forEachChild(sf, (child) => {
    children.push(convertNode(child, sf));
  });

  const cu: KS.KSCompilationUnit = {
    kind: 'CompilationUnit',
    fileName: sf.fileName,
    isDeclarationFile: sf.isDeclarationFile,
    sourceText: sf.getFullText(),
    lineStarts: sf.getLineStarts(),
    languageVariant: sf.languageVariant === ts.LanguageVariant.JSX ? 'JSX' : 'Standard',
    pos: sf.getStart(),
    end: sf.getEnd(),
    text: '',
    children,
  };
  tsToKs.set(sf, cu);
  return cu;
}

