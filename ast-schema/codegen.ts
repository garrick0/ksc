// ── Checker (set by buildKSTree for import resolution) ──

let _checker: ts.TypeChecker | undefined;

function isImportReference(node: ts.Node): boolean {
  if (!_checker || !ts.isIdentifier(node)) return false;
  const sym = _checker.getSymbolAtLocation(node);
  if (!sym) return false;
  if (!(sym.flags & ts.SymbolFlags.Alias)) return false;
  // Verify it's a value-position reference (not type-only, not a definition site)
  const parent = node.parent;
  if (!parent) return false;
  // Skip declaration sites (import specifiers, import clause names, etc.)
  if (ts.isImportSpecifier(parent) || ts.isImportClause(parent) || ts.isNamespaceImport(parent)) return false;
  // Skip type-only positions
  if (ts.isTypeReferenceNode(parent) && parent.typeName === node) return false;
  if (ts.isTypeQueryNode(parent)) return false;
  // Skip definition sites
  if (ts.isVariableDeclaration(parent) && parent.name === node) return false;
  if (ts.isParameter(parent) && parent.name === node) return false;
  if (ts.isFunctionDeclaration(parent) && parent.name === node) return false;
  if (ts.isFunctionExpression(parent) && parent.name === node) return false;
  if (ts.isPropertyAccessExpression(parent) && parent.name === node) return false;
  if (ts.isPropertyAssignment(parent) && parent.name === node) return false;
  if (ts.isBindingElement(parent) && parent.name === node) return false;
  if (ts.isPropertySignature(parent) && parent.name === node) return false;
  if (ts.isTypeAliasDeclaration(parent) && parent.name === node) return false;
  return true;
}

// ── Comment handling ──

function toCommentRange(cr: ts.CommentRange): KS.KSCommentRange {
  return {
    pos: cr.pos,
    end: cr.end,
    kind: cr.kind === ts.SyntaxKind.SingleLineCommentTrivia ? 'SingleLine' : 'MultiLine',
    hasTrailingNewLine: cr.hasTrailingNewLine,
  };
}

function attachComments(ksNode: KS.KSNode, tsNode: ts.Node, sf: ts.SourceFile): void {
  const sourceText = sf.getFullText();
  const leading = ts.getLeadingCommentRanges(sourceText, tsNode.getFullStart());
  const trailing = ts.getTrailingCommentRanges(sourceText, tsNode.getEnd());
  if (leading && leading.length > 0) {
    (ksNode as any).leadingComments = leading.map(toCommentRange);
  }
  if (trailing && trailing.length > 0) {
    (ksNode as any).trailingComments = trailing.map(toCommentRange);
  }
}

// ── Child lookup ──

function findChild<T extends KS.KSNode = KS.KSNode>(
  children: KS.KSNode[],
  tsTarget: ts.Node | undefined,
): T | undefined {
  if (!tsTarget) return undefined;
  return tsToKs.get(tsTarget) as T | undefined;
}

function findChildrenOf<T extends KS.KSNode = KS.KSNode>(
  children: KS.KSNode[],
  tsTargets: ts.NodeArray<ts.Node> | readonly ts.Node[] | undefined,
): T[] {
  if (!tsTargets) return [];
  return (tsTargets as readonly ts.Node[]).map((t) => tsToKs.get(t)!).filter(Boolean) as T[];
}

// ── JSDoc comment extraction ──

function extractJSDocComment(n: any): string {
  if (!n.comment) return '';
  if (typeof n.comment === 'string') return n.comment;
  return '';
}

